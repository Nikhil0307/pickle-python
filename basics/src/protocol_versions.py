import pickle


def Serialize(data_to_serialize, protocol_version=None) -> bytes:
    return pickle.dumps(
        obj=data_to_serialize,
        protocol=(
            pickle.HIGHEST_PROTOCOL if not protocol_version else protocol_version
        )
    )


if __name__ == '__main__':
    data = {
        'a': [1, 2.0, 3 + 4j],
        'b': ("character string", b"this is bytes"),
        'c': {None, True, False}
    }
    serialized_Val = Serialize(data_to_serialize=data)
    print(serialized_Val)

    for i in range(6):  # protocol version from 0-5 are available in pickle module
        temp_store = Serialize(data_to_serialize=data, protocol_version=i)
        print(temp_store)

